        TrustGraphEdge("User", "Application", "ACCESSES",
                       "User accesses application functionality"),
        TrustGraphEdge("Application", "IdentityProvider", "DELEGATES_AUTH_TO",
                       "Application delegates authentication to IdP"),
        TrustGraphEdge("Application", "Service", "DEPENDS_ON",
                       "Application depends on service for functionality"),
        TrustGraphEdge("Service", "Service", "DEPENDS_ON",
                       "Service depends on another service"),
        TrustGraphEdge("Application", "Database", "READS_FROM",
                       "Application reads data from database"),
        TrustGraphEdge("Application", "Database", "WRITES_TO",
                       "Application writes data to database"),
        TrustGraphEdge("Application", "Vendor", "INTEGRATES_WITH",
                       "Application integrates with third-party vendor"),
        TrustGraphEdge("User", "NetworkInfrastructure", "CONNECTS_VIA",
                       "User connects via network infrastructure"),
        TrustGraphEdge("Application", "NetworkInfrastructure", "HOSTED_ON",
                       "Application is hosted on network infrastructure"),
        TrustGraphEdge("Service", "NetworkInfrastructure", "COMMUNICATES_VIA",
                       "Services communicate via network infrastructure"),
        TrustGraphEdge("IdentityProvider", "Vendor", "FEDERATES_WITH",
                       "IdP federates with external identity providers"),
        TrustGraphEdge("Process", "Application", "GOVERNS",
                       "Process governs application changes and operations"),
        TrustGraphEdge("User", "Process", "FOLLOWS",
                       "User follows defined process"),
        TrustGraphEdge("Process", "User", "REVIEWS",
                       "Process reviews user's access and actions"),
        TrustGraphEdge("Device", "NetworkInfrastructure", "ATTACHES_TO",
                       "Device attaches to network"),
        TrustGraphEdge("Device", "Application", "RUNS",
                       "Device runs application or service"),
        TrustGraphEdge("Credential", "User", "IDENTIFIES",
                       "Credential identifies user to systems"),
        TrustGraphEdge("Credential", "Service", "IDENTIFIES",
                       "Credential identifies service to other services"),
